package com.mira.mira.entities;

public class Content {
    public String title;
    public String overview;
    public String language;
    public String type;

    public String getTitle() {
        return title;
    }

    public String getOverview() {
        return overview;
    }

    public String getLanguage() {
        return language;
    }

    public String getType() {
        return this.type;
    }
}
